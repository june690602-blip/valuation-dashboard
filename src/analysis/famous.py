"""[레거시 · Streamlit 전용] 유명 투자자 '대표 전략 원형' — 자산군 비중으로 표현한 교육용 예시.

웹(Meridian) 프런트에서는 사용하지 않는다. Streamlit 구버전(app.py)이 레거시로 동결되면서
이 모듈도 함께 동결 상태다 — 새 기능은 웹 쪽에만 추가한다.

포트폴리오 페이지에서 사용자의 실제 포트폴리오와 σ-기대수익 평면에서 비교하는 '재미+참고' 용도.
비중은 널리 알려진 전략을 우리 자산군(주식/채권/금/리츠/현금)으로 단순 근사한 것이며,
특정 인물의 실제 계좌·수익률이 아니다.
"""
from __future__ import annotations

ASSET_CLASSES = ("주식", "채권", "금", "리츠", "현금")

# 이름 → (자산군 비중 dict, 한 줄 설명). 비중 합 = 1.0
STRATEGIES: dict[str, tuple[dict, str]] = {
    "워런 버핏식 (집중 가치)": (
        {"주식": 0.85, "현금": 0.15},
        "우량주에 집중 + 현금 쿠션. 낮은 회전율·장기 보유. 변동성은 높지만 기업 선별로 방어."),
    "보글 3펀드 (인덱스)": (
        {"주식": 0.60, "채권": 0.40},
        "저비용 인덱스로 시장 전체를 산다. 단순·광범위 분산의 대명사."),
    "레이 달리오 올웨더": (
        {"주식": 0.30, "채권": 0.55, "금": 0.15},
        "경기 국면에 고루 대응(리스크 패리티 근사). 장기채·금으로 하락장 방어."),
    "예일 모델 (스웬슨)": (
        {"주식": 0.30, "채권": 0.30, "리츠": 0.20, "금": 0.20},
        "대체자산 비중↑(여기선 리츠·금으로 근사). 장기·기관형 분산."),
}


def strategy_weights(name: str) -> dict:
    """전략명 → 자산군 비중 dict (없는 자산군은 0)."""
    w, _ = STRATEGIES[name]
    return {c: float(w.get(c, 0.0)) for c in ASSET_CLASSES}
